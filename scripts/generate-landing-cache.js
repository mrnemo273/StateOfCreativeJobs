#!/usr/bin/env node

/**
 * Reads all snapshot JSON files from src/data/snapshots/ and generates
 * src/lib/cachedLandingData.ts with pre-built RoleSummary data.
 *
 * Run after snapshot refresh: node scripts/generate-landing-cache.js
 */

const fs = require("fs");
const path = require("path");

const SNAPSHOT_DIR = path.join(__dirname, "..", "src", "data", "snapshots");
const OUTPUT_FILE = path.join(__dirname, "..", "src", "lib", "cachedLandingData.ts");

const ALL_SLUGS = [
  "creative-director", "design-director", "head-of-design", "vp-of-design", "cco",
  "senior-product-designer", "ux-designer", "product-designer", "ux-researcher",
  "design-systems-designer", "brand-designer", "graphic-designer", "visual-designer",
  "art-director", "motion-designer", "copywriter", "content-strategist", "ux-writer",
  "creative-copywriter", "content-designer",
];

const summaries = [];

for (const slug of ALL_SLUGS) {
  const filePath = path.join(SNAPSHOT_DIR, `${slug}.json`);
  if (!fs.existsSync(filePath)) {
    console.warn(`  SKIP ${slug} — no snapshot file`);
    continue;
  }

  try {
    const raw = fs.readFileSync(filePath, "utf-8");
    const d = JSON.parse(raw);

    summaries.push({
      title: d.title,
      slug: d.slug,
      cluster: d.cluster,
      openingsCount: d.demand.openingsCount,
      yoyChange: d.demand.yoyChange,
      medianSalary: d.salary.medianUSD,
      aiScore: d.aiImpact.score,
      aiLabel: d.aiImpact.scoreLabel,
      sparkline: (d.demand.openingsTrend || []).slice(-6).map((p) => p.value),
      lastUpdated: d.lastUpdated,
    });

    console.log(`  OK   ${slug}`);
  } catch (err) {
    console.warn(`  FAIL ${slug} — ${err.message}`);
  }
}

const timestamp = new Date().toISOString();

const output = `// Auto-generated — do not edit manually.
// Regenerated by the weekly refresh script alongside role snapshots.
// Generated: ${timestamp}

import type { RoleSummary } from "./landingData";

export const CACHED_ROLE_SUMMARIES: RoleSummary[] = ${JSON.stringify(summaries, null, 2)};

export const CACHE_TIMESTAMP = "${timestamp}";
`;

fs.writeFileSync(OUTPUT_FILE, output, "utf-8");
console.log(`\nWrote ${summaries.length} role summaries to ${OUTPUT_FILE}`);
